import 'package:flutter/material.dart';

class AppTheme {
  ThemeData lightTheme() {
    return ThemeData(
      primaryColor: const Color(0xff1e1b62),
      primaryColorDark: const Color(0xfff2c933),
    );
  }

  ThemeData darkTheme() {
    return ThemeData(
      primaryColor: const Color(0xff1e1b62),
      primaryColorDark: const Color(0xfff2c933),
    );
  }
}
